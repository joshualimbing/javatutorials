package com.javatutorial;

public class PrimitiveArrays {

    public static void main(String[] args) {
        int[] numbers = new int[3];

        numbers[0] = 5;
        numbers[1] = 2;
        numbers[2] = 3;

        for (int i = 0; i < 3; i++){
            System.out.println(numbers[i]);
        }

    }

}
